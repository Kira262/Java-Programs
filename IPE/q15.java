class NumberRanges {
    public static void main(String[] args) {
        System.out.println("Numbers from 1 to 10:");
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }

        System.out.println("\nNumbers from 20 to 30:");
        for (int i = 20; i <= 30; i++) {
            System.out.print(i + " ");
        }
    }
}
